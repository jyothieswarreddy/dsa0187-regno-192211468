#include <iostream>
int main() {
    int num1 = 10;
    int num2 = 20;
    int sum = num1 + num2;
    std::cout << "First number: " << num1 << std::endl;
    std::cout << "Second number: " << num2 << std::endl;
    std::cout << "Sum of the two numbers: " << sum << std::endl;
    return 0;
}
/*
output:
First number: 10
Second number: 20
Sum of the two numbers: 30
*/
