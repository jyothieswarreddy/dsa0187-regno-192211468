#include <iostream>

int main() {
    // Integer data type
    int myInteger = 10;
    std::cout << "Integer value: " << myInteger << std::endl;

    // Float data type
    float myFloat = 3.14;
    std::cout << "Float value: " << myFloat << std::endl;

    // Character data type
    char myChar = 'A';
    std::cout << "Character value: " << myChar << std::endl;

    return 0;
}
/*
output:
Integer value: 10
Float value: 3.14
Character value: A
*/
